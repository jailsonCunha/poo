public class Contato {
	
	private String nome;
	private String tel;
	
	public Contato(String nome, String telefone){
		this.nome = nome;
		this.tel = telefone;
	}

	public String getNome() {
		return this.nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}

	public String getTel() {
		return this.tel;
	}
	public void setTel(String tel) {
		this.tel = tel;
	}
	
	

}
